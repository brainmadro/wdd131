const menu$ = document.querySelector(".menu-content");
const navbar$ = document.querySelector("nav");
const sectionTitle$ = document.querySelector("#sectionTitle");
const imagesContainer$ = document.querySelector("#templeImages");
const hamburger$ = document.querySelector(".hamburger");

hamburger$.addEventListener("click", function () {
  hamburger$.classList.toggle("isactive");
  navbar$.classList.toggle("active");
});

for (const element of menu$.children) {
  element.addEventListener("click", (event) => {
    const category = event.target.dataset.category;
    imagesContainer$.innerHTML = "";
    populateGallery(category);
    hamburger$.classList.remove("isactive");
    navbar$.classList.remove("active");
  });
}

function populateGallery(category) {
  if (category == "home") {
    return (sectionTitle$.innerHTML =
      category.charAt(0).toUpperCase() + category.slice(1).toLowerCase());
  }

  const temples = {
    old: oldTemples,
    new: newTemples,
    large: largeTemples,
    small: smallTemples,
  };

  sectionTitle$.innerHTML =
    category.charAt(0).toUpperCase() + category.slice(1).toLowerCase();

  if (imagesContainer$) {
    temples[category].forEach((img) => {
      imagesContainer$.innerHTML += `<figure><img loading="lazy" src="${img.src}" alt="${img.alt}"><figcaption>${img.caption}</figcaption></figure>`;
    });
  }
}

populateGallery("home");
